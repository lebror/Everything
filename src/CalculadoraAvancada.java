
public class CalculadoraAvancada {
	public double calculaCos (double x) {
		return Math.cos(x);
	}
	public double calculaSen (double x) {
		return Math.sin(x);
	}
	public double calculaTan (double x) {
		return Math.tan(x);
	}
	public double calculaSqrt (double x) {
		return Math.sqrt(x);
	}
	public double calculaPotencia (double x, double y) {
		return Math.pow(x,y);
	}
}